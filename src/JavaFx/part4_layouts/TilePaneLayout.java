package JavaFx.part4_layouts;

public class TilePaneLayout {
    /*
     * TilePane Layout
     * .................
     * With TilePane, every cell has been allocated the same width
     * There are gaps between the buttons because some buttons aren't wide enough to occupy the width of their cell
     * Now because each tile has a width of the largest button, there are 3 rows when we first run the application
     *  rather than 2 that we saw when using FlowPane
     * The buttons are centered within each cell & when we resize the window, we'll see the buttons are wrapped and unwrapped
     *  as they did with FlowPanes
     */
}
